//https://binarysearch.com/problems/Contained-Interval
class ContainedInterval {
  static solve(intervals) {
    console.time('ContainedInterval');
    intervals = intervals.sort((a, b) => a[0] - b[0]);

    for (let i = 0; i < intervals.length; i++) {
        const [start, end] = intervals[i];
        const len = end - start;

        let leftIndex = i - 1;

        while (leftIndex > -1) {
            const [startF, endF] = intervals[leftIndex];
            const lenF = endF - startF;

            if (start === startF) {
                return true;
            }

            if (endF < start) {
                break;
            }

            if (lenF - (start - startF) >= len) {
                return true;
            }

            leftIndex--;
        }
    }

    console.timeEnd('ContainedInterval')
    return false;
  }
}

